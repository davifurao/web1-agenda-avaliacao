CREATE DATABASE WEB1_CONTATOS;

USE WEB1_CONTATOS;

CREATE TABLE USR_usuarios (
    USR_USER_ID INT NOT NULL AUTO_INCREMENT,
    USR_NOME VARCHAR(50),
    USR_EMAIL VARCHAR(60),
    USR_SENHA VARCHAR(60),
    PRIMARY KEY(USR_USER_ID)
);

CREATE TABLE CONT_contatos (
    CONT_USER_ID  INT NOT NULL AUTO_INCREMENT,
    CONT_NUMERO VARCHAR(20),
    CONT_EMAIL VARCHAR (50),
    CONT_ENDERECO varchar (100),
    CONT_NOME VARCHAR(50),
    USR_CONT_FK INT ,
    PRIMARY KEY (CONT_USER_ID),
    FOREIGN KEY (USR_CONT_FK) REFERENCES USR_usuarios(USR_USER_ID)
);